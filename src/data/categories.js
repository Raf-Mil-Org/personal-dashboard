// Helper function to get category by description
export const getCategoryByDescription = (description) => {
    return categories.find(cat => cat.description === description);
};

// Helper function to get subcategory by description within a category
export const getSubcategoryByDescription = (categoryDescription, subcategoryDescription) => {
    const category = getCategoryByDescription(categoryDescription);
    if (category) {
        return category.subCategories.find(sub => sub.description === subcategoryDescription);
    }
    return null;
};

// Helper function to get all category descriptions
export const getCategoryDescriptions = () => {
    return categories.map(cat => cat.description);
};

// Helper function to get subcategory descriptions for a given category
export const getSubcategoryDescriptions = (categoryDescription) => {
    const category = getCategoryByDescription(categoryDescription);
    return category ? category.subCategories.map(sub => sub.description) : [];
};

// Helper function to get all subcategory descriptions
export const getAllSubcategoryDescriptions = () => {
    return categories.flatMap(cat => cat.subCategories.map(sub => sub.description));
}; 